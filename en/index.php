        Do you have a dream? Do you work with what you love? Do you still remember what you dreamed of when you were younger? Do you believe everyone has the right to work with what they want to, and do you believe everyone would be capable to do so?
      </p>
      <p>
        My aim with this website as a final project on my studies on human rights is to show that <strong>you can't put labels on people according to only their skin color</strong>, and believe that all of them are the same. People in Sweden don't all do and dream of the same things, neither does everyone on Zanzibar or anywhere else in the world. We all are individuals with our own dreams and goals in life, even if the societies we grow up in of course affect us (this <a href="http://dreamingindividuals.net/diagram.jpg">graph</a>, by <a href="http://www.worldvaluessurvey.org/wvs.jsp">World Values Survey</a>, is a good reminder of the basic values in different countries).
      </p>
      <p>
        Especially when it comes to working with specific jobs, the society and it's expectations and norms have a big role. Everywhere you can find those stereotypes saying women or people of specific skin colors are less expected to work in some roles than men or people of other skin colors, and vice versa. But different jobs are also differently valued, and therefore people working with different jobs are also stereotyped and valued accordingly. <strong>But behind all those stereotypes, there still are different people with different thoughts, capabilites and interests: maybe even people who dream of working with something else, but for one or another reason still don't do that</strong> - maybe because of expectations from their families, maybe to be able to provide their family economically with the current job, maybe because reschooling would be too expensive, maybe they have been told that their dreams are impossible, or maybe it's the structures in the society that they don't get the jobs they want to have and apply for.
      </p>
      <p>
        That's why we should all respect and meet every person as themselves, as just as unique individuals as we see ourselves as: stop putting labels on people and stop telling people their dreams or choices of work are stupid, unworthy or impossible. <strong>Stop pushing down each others, and instead see people's strengths and capacities, and treat differences as a way of thinking outside the box, that can lead to development and refreshed thoughts</strong> (like the study "<a href="http://dreamingindividuals.net/Does-Diversity-Pay-Race-Gender-and-the-Business-Case-for-Diversity.pdf">Does Diversity Pay?: Race, Gender, and the Business Case for Diversity</a>" by Cedric Herring shows).
      </p>
      <p>
        Happy reading on my website, keep on dreaming and don't let people push you down - and don't push anyone else down!
